const base64Chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/'

function toBase64(str: string) {
  let result = ''
  let i = 0
  while (i < str.length) {
    const a = str.charCodeAt(i++)
    const b = i < str.length ? str.charCodeAt(i++) : NaN
    const c = i < str.length ? str.charCodeAt(i++) : NaN
    const bitmap = (a << 16) | ((isNaN(b) ? 0 : b) << 8) | (isNaN(c) ? 0 : c)
    result += base64Chars.charAt((bitmap >> 18) & 63)
    result += base64Chars.charAt((bitmap >> 12) & 63)
    result += isNaN(b) ? '=' : base64Chars.charAt((bitmap >> 6) & 63)
    result += isNaN(c) ? '=' : base64Chars.charAt(bitmap & 63)
  }
  return result
}

function getDataUrl(name: IconName, color: string) {
  const svg = svgMap[name].replace(/currentColor/g, color)
  return `data:image/svg+xml;base64,${toBase64(svg)}`
}

interface IconProps {
  name: IconName
  size?: number
  color?: string
  className?: string
}

export default function Icon({ name, size = 28, color = '#0F172A', className = '' }: IconProps) {
  const src = getDataUrl(name, color)
  const sizePx = Taro.pxTransform(size)
  return (
    <View
      className={`icon ${className}`}
      style={{
        width: sizePx,
        height: sizePx,
        backgroundImage: `url("${src}")`,
        backgroundSize: 'contain',
        backgroundRepeat: 'no-repeat',
        backgroundPosition: 'center'
      }}
    />
  )
}
